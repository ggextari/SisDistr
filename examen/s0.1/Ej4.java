public class Ej4 {

    public static void main(String[] args) {
        // args es un array de cadenas que contiene los argumentos

        // Iterar sobre cada argumento y imprimirlo
        for (int i = 0; i < args.length; i++) {
            System.out.println("Argumento " + i + ": " + args[i]);
        }
    }
}
